<script type="text/javascript">
           
    $('#contact-form').validator();

    // when the form is submitted
    $('#contact-form').on('submit', function (e) {
        // $('#btn-send').prop("value", "Sending");

        // if the validator does not prevent form submit
        if (!e.isDefaultPrevented()) {
            var url = "contact.php";

            //get values
            var form_name=$("#name").val();
            var form_email=$("#email").val();
            var form_message=$("#message").val();

            // POST values in the background the the script URL
            $.ajax({
                type: "POST",
                url: url,
                data: {"name":form_name,"email":form_email,"message":form_message},
                success: function (data){
                     
                    // data = JSON object that contact.php returns
                    // we recieve the type of the message: success x danger and apply it to the 
                    var messageAlert = 'alert-' + data.type;
                    var messageText = data.message;

                    
                    // let's compose Bootstrap alert box HTML
                    var alertBox = '<div class="alert ' + messageAlert + ' alert-dismissable"><button type="button" class="close" data-dismiss="alert" aria-hidden="true">&times;</button>' + messageText + '</div>';
                    
                    // If we have messageAlert and messageText
                    if (messageAlert && messageText) {
                        // inject the alert to .messages div in our form
                        $('#contact-form').find('#message_result').html(alertBox);
                        
                        // empty the form
                        $('#contact-form').find('input:text').val('');                    
                        $('message').val('');
                    }
                }
            });
            return false;
        }
    })

        </script>

